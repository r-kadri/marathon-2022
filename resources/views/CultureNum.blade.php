De plus en plus répandu, les exposition virtuelles permettent l’accès 

Ce type d’exposition 100% virtuelle est de plus en plus répandu dans le monde de l’art, de nombreux musées ont déjà adopté cette méthode sur leur site web. Cela est très intéressant car elle permet une accessibilité accrue à l'art pour chacun car ces visites sont gratuites, elles permettent de visiter des musées qui se trouvent à l’autre bout du monde.

Certaines personnes le voient comme si une œuvre n'était plus le résultat du talent d’une personne mais de toute une équipe d’ingénieurs de designer etc… ce qui dénaturerait la notion classique de l’art. Pourtant l’art numérique existe depuis bien longtemps car dans les années 1950 un mathématicien américain réalise les premières images graphiques réalisées sur ordinateur. Dans ces années l’art numérique était plus un long calcul mathématique mais beaucoup ont commencé a adopté ces méthodes et en 1968 la première exposition d’art numérique a lieu à Londres. Depuis ces années l’art digital n'a cessé de s’améliorer avec la retouche photo à la suite des photo numérique, le dessin vectoriel qui permet de redimensionner à souhait sans perdre de qualité. 

L’art digitale n’as fait qu'évoluer depuis les années 1950 en suivant les innovations technologiques, elle a permis une diversification des pratiques et une forte croissance de l’accessibilité cependant l’art digitale reste encore très controversé car l’art perd son charme de l’homme face à sa peinture ou sa sculpture travaillant pendant des heures avec ses outils.

