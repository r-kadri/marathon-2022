        <h1>L.ART</h1>
        <p>Critique de la nouvelle exposition digitale Pixel Artrip

Pour : 
Cette exposition montre bien le futur de l’art, ce sur quoi les artistes de demain vont se pencher grâce à la visite de musée virtuel ou encore de génération d'œuvre d’art grâce aux intelligences artificielles. Les musées virtuels apportent un nouveau regard sur l’accès à l'art avec des visites gratuites disponibles partout dans le monde, depuis la crise sanitaire on s’est bien rendu compte que le numérique est la solution pour améliorer l’accessibilité à la culture. L’exposition de Pixel Artrip nous permet de découvrir le monde a travers un musée virtuel avec des œuvres sous forme de pixel art ce qui nous permet de faire le tour du monde en restant bien au chaud chez soi. Les musées virtuels nous permettent de découvrir l’art sous un nouvel angle.


Contre : 

Il n’y a pas de notion d’art dès lors qu’on parle d’image générée par une intelligence artificielle qui se contente de retranscrire sous forme d’image les idées qu’on lui suggère sous forme de texte. Pour qu'une œuvre d’art soit recevable en tant que tel elle doit venir d’une longue réflexion est d’un travail précis et minutieux d’un artiste afin de faire passer un message et transmettre des émotions. Les musées virtuels ne sont là que pour faire la promotion des sites web des musées, rien ne vaut une vrai visite d’un musée, prendre le temps en contemplant les œuvres devant soi, partager ce moment avec des proches et non pas seul chez soi derrière un écran. 

